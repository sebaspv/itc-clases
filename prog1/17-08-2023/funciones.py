import math

print(math.ceil(3.4)) # te da el entero *hacia arriba* más próximo, por lo que saldría 4
print(math.cos(1*math.pi)) # existen funciones trigonométricas, específicamente se da en radianes
print(math.fabs(2.4)) # valor absoluto de floats
print(math.factorial(3)) # número factorial
print(math.floor(3.7))